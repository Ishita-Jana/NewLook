package com.example.newlook.shared.POJO;

public class ArtistAllDetailsModel {
    String name;
    String email;
    String bio;
    String responseTime;
    String skills;
    int artistImg;
    Double rating;
    String charges;
    String categories;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getBio() {
        return bio;
    }

    public String getResponseTime() {
        return responseTime;
    }

    public String getSkills() {
        return skills;
    }

    public int getArtistImg() {
        return artistImg;
    }

    public Double getRating() {
        return rating;
    }

    public String getCategories() {
        return categories;
    }

    public String getCharges() {
        return charges;
    }
}
